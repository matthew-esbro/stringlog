/* StringLog platform bridge + share/export generation.
 * Uses Capacitor plugins (Clipboard, Filesystem, Share) when running natively,
 * and falls back to web APIs (clipboard / download) so it also works in a browser.
 * Pure logic, no UI.
 */
(function () {
  'use strict';
  var SL = (window.SL = window.SL || {});
  var U = SL.util, DS = SL.DataService;

  function plugins() { return (window.Capacitor && window.Capacitor.Plugins) || {}; }
  function isNative() { return !!(window.Capacitor && window.Capacitor.isNativePlatform && window.Capacitor.isNativePlatform()); }

  function sanitize(s) { return String(s || 'racquet').replace(/[^a-z0-9]+/gi, '_').replace(/^_|_$/g, '').toLowerCase() || 'racquet'; }

  // ---------------- profile data assembled for sharing ----------------
  function buildProfile(racquetId) {
    var s = DS.getSettings();
    var r = DS.getRacquet(racquetId);
    var jobs = DS.listStringJobs({ racquetId: racquetId }); // newest first
    var current = DS.currentStringJob(racquetId);
    var previous = jobs.filter(function (j) { return !current || j.id !== current.id; }).slice(0, 3);
    return { settings: s, racquet: r, current: current, previous: previous };
  }
  function tension(lbs, units) { return U.displayTension(lbs, units); }

  function buildText(racquetId) {
    var p = buildProfile(racquetId), r = p.racquet, s = p.settings, L = [];
    L.push('StringLog — Racquet Profile'); L.push('');
    if (s.player_name) L.push('Player: ' + s.player_name);
    L.push('Racquet: ' + r.brand + ' ' + r.model);
    if (r.grip_size) L.push('Grip size: ' + r.grip_size);
    L.push('');
    if (p.current) {
      var c = p.current;
      L.push('CURRENT SETUP');
      L.push('Mains: ' + c.mains_string_brand + ' ' + c.mains_string_model + (c.mains_gauge ? ' ' + c.mains_gauge : '') + ' @ ' + tension(c.mains_tension_lbs, s.units));
      if (c.crosses_same_as_mains) L.push('Crosses: same as mains');
      else if (c.crosses_string_brand) L.push('Crosses: ' + c.crosses_string_brand + ' ' + c.crosses_string_model + (c.crosses_gauge ? ' ' + c.crosses_gauge : '') + ' @ ' + tension(c.crosses_tension_lbs, s.units));
      L.push('Last strung: ' + U.formatDate(c.date_strung));
      if (c.date_broke) L.push('Note: these strings broke ' + U.formatDate(c.date_broke) + '.');
    } else { L.push('No current string setup logged.'); }
    if (r.recommended_tension_min && r.recommended_tension_max)
      L.push('Recommended tension: ' + tension(r.recommended_tension_min, s.units) + ' – ' + tension(r.recommended_tension_max, s.units));
    if (p.previous.length) {
      L.push(''); L.push('PREVIOUS SETUPS');
      p.previous.forEach(function (j) { L.push('• ' + U.formatDate(j.date_strung) + ': ' + j.mains_string_brand + ' ' + j.mains_string_model + ' @ ' + tension(j.mains_tension_lbs, s.units)); });
    }
    if (r.notes) { L.push(''); L.push('Player notes: ' + r.notes); }
    L.push(''); L.push('Generated by StringLog');
    return L.join('\n');
  }

  // ---------------- dark/gold PNG card (canvas) ----------------
  function wrap(ctx, text, maxW) {
    var words = String(text).split(/\s+/), lines = [], cur = '';
    words.forEach(function (w) {
      var t = cur ? cur + ' ' + w : w;
      if (ctx.measureText(t).width > maxW && cur) { lines.push(cur); cur = w; } else cur = t;
    });
    if (cur) lines.push(cur);
    return lines;
  }

  function drawCard(racquetId) {
    var p = buildProfile(racquetId), r = p.racquet, s = p.settings;
    var W = 1080, H = 1500, pad = 84;
    var cv = document.createElement('canvas'); cv.width = W; cv.height = H;
    var ctx = cv.getContext('2d');
    var GOLD = '#e2c07a', TEXT = '#f2f2f2', DIM = '#9a9a9a', FONT = '-apple-system, "Helvetica Neue", Arial, sans-serif';
    ctx.fillStyle = '#0a0a0a'; ctx.fillRect(0, 0, W, H);
    ctx.strokeStyle = '#2a2a2a'; ctx.lineWidth = 2; ctx.strokeRect(26, 26, W - 52, H - 52);
    var y = pad + 26;
    function txt(t, size, color, weight, gap) {
      ctx.fillStyle = color; ctx.font = (weight || '400') + ' ' + size + 'px ' + FONT;
      ctx.fillText(t, pad, y); y += size + (gap == null ? 20 : gap);
    }
    function rule() { ctx.strokeStyle = GOLD; ctx.lineWidth = 2; ctx.beginPath(); ctx.moveTo(pad, y); ctx.lineTo(W - pad, y); ctx.stroke(); y += 40; }
    txt('StringLog', 34, GOLD, '700', 4);
    txt('Racquet Profile', 27, DIM, '400', 26);
    rule();
    if (s.player_name) txt(s.player_name, 30, DIM, '400', 22);
    ctx.font = '700 54px ' + FONT;
    wrap(ctx, r.brand + ' ' + r.model, W - pad * 2).slice(0, 2).forEach(function (ln) { txt(ln, 54, TEXT, '700', 12); });
    if (r.grip_size) txt('Grip ' + r.grip_size, 30, DIM, '400', 36);
    if (p.current) {
      var c = p.current;
      txt('CURRENT SETUP', 24, GOLD, '700', 20);
      txt('Mains', 26, DIM, '400', 8);
      txt(c.mains_string_brand + ' ' + c.mains_string_model + (c.mains_gauge ? ' ' + c.mains_gauge : ''), 38, TEXT, '600', 6);
      txt(tension(c.mains_tension_lbs, s.units), 56, GOLD, '800', 22);
      if (!c.crosses_same_as_mains && c.crosses_string_brand) {
        txt('Crosses', 26, DIM, '400', 8);
        txt(c.crosses_string_brand + ' ' + c.crosses_string_model + (c.crosses_gauge ? ' ' + c.crosses_gauge : ''), 38, TEXT, '600', 6);
        txt(tension(c.crosses_tension_lbs, s.units), 48, GOLD, '800', 22);
      } else {
        txt('Crosses: same as mains', 28, DIM, '400', 22);
      }
      txt('Last strung ' + U.formatDate(c.date_strung) + (c.date_broke ? '  ·  broke ' + U.formatDate(c.date_broke) : ''), 28, DIM, '400', 30);
    }
    if (r.recommended_tension_min && r.recommended_tension_max) {
      txt('Recommended tension', 24, GOLD, '700', 8);
      txt(tension(r.recommended_tension_min, s.units) + ' – ' + tension(r.recommended_tension_max, s.units), 34, TEXT, '600', 30);
    }
    if (p.previous.length) {
      txt('PREVIOUS SETUPS', 24, GOLD, '700', 16);
      p.previous.forEach(function (j) {
        txt(U.formatDate(j.date_strung) + '  ·  ' + j.mains_string_brand + ' ' + j.mains_string_model + ' @ ' + tension(j.mains_tension_lbs, s.units), 27, TEXT, '400', 12);
      });
      y += 16;
    }
    if (r.notes) {
      txt('Notes', 24, GOLD, '700', 12);
      ctx.fillStyle = DIM; ctx.font = '400 27px ' + FONT;
      wrap(ctx, r.notes, W - pad * 2).slice(0, 4).forEach(function (ln) { ctx.fillText(ln, pad, y); y += 36; });
    }
    // footer
    ctx.fillStyle = DIM; ctx.font = '400 24px ' + FONT;
    ctx.fillText('Generated by StringLog', pad, H - pad + 6);
    return cv;
  }

  // ---------------- clean printable PDF (jsPDF; loaded on demand) ----------------
  var jspdfLoading = null;
  function ensureJsPDF() {
    if (window.jspdf && window.jspdf.jsPDF) return Promise.resolve(true);
    if (!jspdfLoading) {
      jspdfLoading = new Promise(function (resolve) {
        var sc = document.createElement('script');
        sc.src = 'vendor/jspdf.umd.min.js';
        sc.onload = function () { resolve(true); };
        sc.onerror = function () { jspdfLoading = null; resolve(false); };
        document.head.appendChild(sc);
      });
    }
    return jspdfLoading;
  }

  function buildPDF(racquetId) {
    var p = buildProfile(racquetId), r = p.racquet, s = p.settings;
    var jsPDF = window.jspdf && window.jspdf.jsPDF;
    var doc = new jsPDF({ unit: 'pt', format: 'letter' });
    var W = doc.internal.pageSize.getWidth(), pageH = doc.internal.pageSize.getHeight(), m = 56, y = 72;
    function ensureSpace(needed) { if (y + needed > pageH - m) { doc.addPage(); y = 72; } }
    doc.setFont('helvetica', 'bold'); doc.setFontSize(22); doc.setTextColor(20, 20, 20); doc.text('StringLog', m, y);
    doc.setFont('helvetica', 'normal'); doc.setFontSize(13); doc.setTextColor(120, 120, 120); doc.text('Racquet Profile', m, y + 20);
    y += 38; doc.setDrawColor(200, 165, 90); doc.setLineWidth(2); doc.line(m, y, W - m, y); y += 30;
    function h2(t) { ensureSpace(38); doc.setFont('helvetica', 'bold'); doc.setFontSize(11); doc.setTextColor(150, 115, 35); doc.text(t.toUpperCase(), m, y); y += 18; }
    function row(label, val) { if (val == null || val === '') return; ensureSpace(19); doc.setFont('helvetica', 'normal'); doc.setFontSize(12); doc.setTextColor(110, 110, 110); doc.text(label, m, y); doc.setTextColor(20, 20, 20); doc.text(String(val), m + 150, y); y += 19; }
    if (s.player_name) row('Player', s.player_name);
    row('Racquet', r.brand + ' ' + r.model);
    row('Grip size', r.grip_size);
    if (r.recommended_tension_min && r.recommended_tension_max) row('Rec. tension', tension(r.recommended_tension_min, s.units) + ' - ' + tension(r.recommended_tension_max, s.units));
    y += 10;
    if (p.current) {
      var c = p.current; h2('Current Setup');
      row('Mains', c.mains_string_brand + ' ' + c.mains_string_model + (c.mains_gauge ? ' ' + c.mains_gauge : '') + ' @ ' + tension(c.mains_tension_lbs, s.units));
      row('Crosses', c.crosses_same_as_mains ? 'Same as mains' : (c.crosses_string_brand ? (c.crosses_string_brand + ' ' + c.crosses_string_model + (c.crosses_gauge ? ' ' + c.crosses_gauge : '') + ' @ ' + tension(c.crosses_tension_lbs, s.units)) : ''));
      row('Last strung', U.formatDate(c.date_strung));
      if (c.date_broke) row('Strings broke', U.formatDate(c.date_broke));
      if (c.pre_stretch_applied) row('Pre-stretch', 'Yes');
      y += 10;
    }
    if (p.previous.length) {
      h2('Previous Setups');
      p.previous.forEach(function (j) { row(U.formatDate(j.date_strung), j.mains_string_brand + ' ' + j.mains_string_model + ' @ ' + tension(j.mains_tension_lbs, s.units)); });
      y += 10;
    }
    if (r.notes) {
      h2('Player Notes');
      doc.setFont('helvetica', 'normal'); doc.setFontSize(12); doc.setTextColor(40, 40, 40);
      doc.splitTextToSize(r.notes, W - m * 2).forEach(function (ln) { ensureSpace(16); doc.text(ln, m, y); y += 16; });
    }
    doc.setFont('helvetica', 'normal'); doc.setFontSize(10); doc.setTextColor(150, 150, 150);
    doc.text('Generated by StringLog', m, doc.internal.pageSize.getHeight() - 40);
    return doc;
  }

  // ---------------- output helpers (native vs web) ----------------
  function copyText(text) {
    var P = plugins();
    if (P.Clipboard) return P.Clipboard.write({ string: text }).then(function () { return { ok: true, method: 'clipboard' }; });
    if (navigator.clipboard && navigator.clipboard.writeText) return navigator.clipboard.writeText(text).then(function () { return { ok: true, method: 'clipboard' }; });
    return Promise.resolve({ ok: false });
  }

  function shareDataUrl(filename, dataUrl, mime) {
    var P = plugins();
    var base64 = dataUrl.indexOf(',') >= 0 ? dataUrl.split(',')[1] : dataUrl;
    if (P.Filesystem && P.Share) {
      return P.Filesystem.writeFile({ path: filename, data: base64, directory: 'CACHE' })
        .then(function () { return P.Filesystem.getUri({ path: filename, directory: 'CACHE' }); })
        .then(function (res) { return P.Share.share({ title: 'StringLog Racquet Profile', files: [res.uri] }); })
        .then(function () { return { ok: true, method: 'share' }; })
        .catch(function (e) { return { ok: false, error: String(e) }; });
    }
    // web fallback: trigger a download so it is testable in a browser
    var a = document.createElement('a'); a.href = dataUrl; a.download = filename;
    document.body.appendChild(a); a.click(); a.remove();
    return Promise.resolve({ ok: true, method: 'download' });
  }

  function share(racquetId, mode) {
    var r = DS.getRacquet(racquetId);
    var base = 'stringlog_' + sanitize(r && (r.nickname || r.model));
    if (mode === 'text') return copyText(buildText(racquetId));
    if (mode === 'image') return shareDataUrl(base + '.png', drawCard(racquetId).toDataURL('image/png'), 'image/png');
    if (mode === 'pdf') {
      return ensureJsPDF().then(function (ok) {
        if (!ok) return { ok: false, error: 'pdf library failed to load' };
        return shareDataUrl(base + '.pdf', buildPDF(racquetId).output('datauristring'), 'application/pdf');
      });
    }
    return Promise.resolve({ ok: false });
  }

  // Share multiple text files (CSV export). Native: write + share sheet. Web: download each.
  function shareTextFiles(files) {
    var P = plugins();
    if (P.Filesystem && P.Share) {
      return Promise.all(files.map(function (f) {
        return P.Filesystem.writeFile({ path: f.name, data: f.data, directory: 'CACHE', encoding: 'utf8' })
          .then(function () { return P.Filesystem.getUri({ path: f.name, directory: 'CACHE' }); })
          .then(function (u) { return u.uri; });
      })).then(function (uris) { return P.Share.share({ title: 'StringLog Data Export', files: uris }); })
        .then(function () { return { ok: true, method: 'share' }; })
        .catch(function (e) { return { ok: false, error: String(e) }; });
    }
    files.forEach(function (f) {
      var blob = new Blob([f.data], { type: f.mime || 'text/csv' });
      var url = URL.createObjectURL(blob);
      var a = document.createElement('a'); a.href = url; a.download = f.name;
      document.body.appendChild(a); a.click(); a.remove(); URL.revokeObjectURL(url);
    });
    return Promise.resolve({ ok: true, method: 'download' });
  }

  function openUrl(url) {
    var P = plugins();
    if (P.Browser && P.Browser.open) { P.Browser.open({ url: url }); return; }
    try { window.open(url, '_blank'); } catch (e) { location.href = url; }
  }

  // ---------------- haptics (no-op in a browser) ----------------
  function haptic(kind) {
    var P = plugins();
    if (!P.Haptics) return;
    try {
      if (kind === 'success' || kind === 'warning' || kind === 'error') {
        P.Haptics.notification({ type: kind.toUpperCase() });
      } else {
        // one-off selection ticks use a light impact: the plugin's selectionChanged
        // is silent unless selectionStart was called first
        P.Haptics.impact({ style: 'LIGHT' });
      }
    } catch (e) {}
  }

  // Open a file picker and read the chosen file as text. Works in WKWebView
  // (iOS shows the document picker) and in a regular browser.
  function pickTextFile(accept) {
    return new Promise(function (resolve) {
      var input = document.createElement('input');
      input.type = 'file';
      if (accept) input.accept = accept;
      input.style.display = 'none';
      input.onchange = function () {
        var f = input.files && input.files[0];
        input.remove();
        if (!f) { resolve(null); return; }
        var reader = new FileReader();
        reader.onload = function () { resolve(String(reader.result)); };
        reader.onerror = function () { resolve(null); };
        reader.readAsText(f);
      };
      document.body.appendChild(input);
      input.click();
    });
  }

  function getAppVersion() {
    var P = plugins();
    if (P.App && P.App.getInfo) {
      return P.App.getInfo()
        .then(function (i) { return i.version + (i.build ? ' (' + i.build + ')' : ''); })
        .catch(function () { return null; });
    }
    return Promise.resolve(null);
  }

  SL.platform = {
    isNative: isNative, copyText: copyText, shareDataUrl: shareDataUrl, share: share,
    shareTextFiles: shareTextFiles, openUrl: openUrl, pickTextFile: pickTextFile, getAppVersion: getAppVersion,
    haptic: haptic, buildText: buildText, drawCard: drawCard, buildProfile: buildProfile
  };
})();
